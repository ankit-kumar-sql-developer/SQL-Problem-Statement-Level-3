; with cte as (
select game_id,
case when COUNT(interaction_type)=0 then 'No Social Intercation' 
when COUNT(distinct case when interaction_type is not null then user_id end)=1 then 'One side intercation'
when COUNT(distinct case when interaction_type is not null then user_id end)=2 
and  COUNT(distinct case when interaction_type ='custom_typed' then user_id end)=0
then 'both side intercation without custom typed'
when COUNT(distinct case when interaction_type is not null then user_id end)=2 
and  COUNT(distinct case when interaction_type ='custom_typed' then user_id end)>=1
then 'both side intercation without custom message'
end as game_type
from #user_interactions
group by game_id )

select game_id,COUNT(*)*100.0/COUNT(*) over() as cnt
from cte group by game_id
